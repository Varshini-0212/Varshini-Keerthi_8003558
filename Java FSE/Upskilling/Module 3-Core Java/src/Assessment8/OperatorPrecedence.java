package Assessment8;

public class OperatorPrecedence {

    public static void main(String[] args) {

        // Expression 1

        int result1 = 10 + 5 * 2;

        // Expression 2

        int result2 = (10 + 5) * 2;

        // Expression 3

        int result3 = 20 / 5 + 3 * 4;

        // Display Results

        System.out.println("Result 1 = " + result1);

        System.out.println("Result 2 = " + result2);

        System.out.println("Result 3 = " + result3);

    }

}